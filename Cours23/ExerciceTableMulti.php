a) Faites un script TableMulti.php qui affiche la table de multiplication d’un nombre envoyé en paramètres GET. (Utiliser la fonction contenue dans Fonctions.php)

b) Faites une liste de liens qui pointe vers le script TableMulti.php, chaque lien faisant afficher la table de multiplication d’un nombre de 1 à 12.

c) Faites un formulaire qui permet de faire afficher la table de multiplication d’un nombre entré par l’usager en appelant le script TableMulti.php.

d) Pour le formulaire, ajouter la validation que la valeur entrée est un nombre.

e) Si vous voulez, vous pouvez faire aussi une version du formulaire qui pointe sur lui-même à la place de pointer sur un autre fichier. (Le résultat s'afficherait en dessous du formulaire, comme dans l'exemple ExempleFormComplexe.php)